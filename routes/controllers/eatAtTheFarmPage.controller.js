/**
 * @module routes/controllers/eatAtTheFarmPage
 * @fileoverview eatAtTheFarmPage route's controller. Exports functions
 *    to be used by each route handler.
 * @exports {Object} Functions to attach to the `eatAtTheFarmPage` router.
 * @require session-helper
 */

 /* sessions
  * This is to help with handling sessions to maintain cart and auth
  */
 const sessions = require('../../scripts/session-helper.js');

/**
 * @function sendEatAtTheFarmPage
 * @description Send the base page rendered by `Handlebars.js`. Compilation
 *    of HBS templates is handled by the HBS engine.
 * @param {Object} req The request object generated by `Express`.
 * @param {Object} res The response object generated by `Express`.
 * @param {Function} next The function to call when this method is done executing
 *    and does not return or render anything (no `res` methods called).
 */
const sendEatAtTheFarmPage = (req, res, next) => {
  // updates the user's cookies and validates them
  sessions.handleSession(req.cookies)
    .then(sessionId => {
      res.cookie("sessionId", sessionId);
      sessions.handleSessionIsLoggedIn(sessionId)
        .then(isLoggedIn => {
          res.render('eat_at_the_farm', {
            title: 'Sprout Creek Farm Eat At The Farm',
            page: 'Eat At The Farm',
            "isLogged": isLoggedIn});
        });
    });
};

//This allows us to use this function on differen pages
module.exports = {
  sendEatAtTheFarmPage,
};
