/**
 * @module routes/controllers/API_getAllAccounts
 * @fileoverview API_getAllAccounts route's controller. Handle all business logic
 * relative to API_getAllAccounts for the users.
 * @exports {Object} Functions to attach to the `API_getAllAccounts` router.
 * @require read-hidden
 * @require cookie-helper
 */

 /* hidden
  * This is to read the hidden credentials file
  */
 var hidden = require('../../scripts/read-hidden.js');

 /* cookies
  * This is to help with handle cookies for user validation
  */
 const cookies = require('../../scripts/cookie-helper.js');


/**
 * @function getAllAccounts
 * @description Get information for all accounts
 * @param {Object} req The request object generated by `Express`.
 * @param {Object} res The response object generated by `Express`.
 * @param {Function} next The function to call when this method is done executing
 *    and does not return or render anything (no `res` methods called).
 */
const getAllAccounts = (req, res, next) => {
  //  read creds from the secret file
  hidden.readHidden()
    .then(json => {
      // updates and validates the user's cookies
      var cookie = {"CID" : req.body}
      console.log("here1");
      //console.log(req.cookies);
      cookies.handleNormalPageCookie(cookie)
        .then(res_cookie => {
          if (res_cookie == null || res_cookie == "undefined") {
            res.clearCookie("CID");
            res.status(401);
            res.send();
          } else {
            console.log("here2");
            res.cookie("CID", res_cookie);
            // required packages for db connection and api call
            var mysql = require("mysql");
            var request = require("request");

            // connect to the database
            var con = mysql.createConnection({
              host: json[0]["host"],
              user: json[0]["user"],
              password: json[0]["password"],
              database: json[0]["database"],
              dateStrings: true
            });
            con.connect(function(err) {
              if (err) {
                console.log(err);
                res.setHeader('Content-Type', 'plain/text');
                res.status(400);
                res.send();
              }
            });
            // get all the account info for a user
            console.log("here3");
            var statement = ("SELECT * from accounts");
            con.query(statement, function(err, result) {
              if (err) {
                console.log(err);
                res.status(400);
                res.setHeader('Content-Type', 'plain/text');
                con.end();
                res.send("getting account info failed!");
              // got account data
              } else if (result.length > 0) {
                console.log("here4");
                res.status(200);
                res.setHeader('Content-Type', 'plain/text');
                con.end();
                console.log(result);
                var data = result
                res.send(data);
              } else {
                console.log(err);
                res.status(400);
                res.setHeader('Content-Type', 'plain/text');
                con.end();
                res.send("no accounts exist failed!");
              }
            });
          }
        });
      });
};

// so other files can call this function
module.exports = {
  getAllAccounts,
};
